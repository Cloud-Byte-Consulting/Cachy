func TestNativeStructuredCompressorMinifiesValidJSON(t *testing.T) {
	t.Parallel()

	original := `{
		"status": "failed",
		"items": [
			{
				"name": "database",
				"error": "timeout"
			}
		]
	}`

	proposal, err := NativeStructuredCompressor{}.Compress(Block{
		ID:          "block_1",
		ContentType: ContentJSON,
		Text:        original,
	})
	if err != nil {
		t.Fatalf("Compress() error = %v", err)
	}

	if len(proposal.Text) >= len(original) {
		t.Fatalf("proposal length = %d, want shorter than %d", len(proposal.Text), len(original))
	}
	if !json.Valid([]byte(proposal.Text)) {
		t.Fatalf("proposal is not valid JSON: %q", proposal.Text)
	}
	if !strings.Contains(proposal.Text, `"error":"timeout"`) {
		t.Fatalf("proposal missing JSON signal: %q", proposal.Text)
	}
}

